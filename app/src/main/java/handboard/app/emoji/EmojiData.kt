    fun getRecent(): List<String> = recentEmojis.toList()

    fun addRecent(emoji: String) {
        recentEmojis.remove(emoji)
        recentEmojis.add(0, emoji)
        if (recentEmojis.size > 30) recentEmojis.removeAt(recentEmojis.lastIndex)
    }

